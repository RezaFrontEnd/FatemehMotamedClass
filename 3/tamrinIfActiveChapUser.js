let users = [
    { name: "Ali", isActive: true },
    { name: "Sara", isActive: false },
    { name: "Reza", isActive: true }
];
for(x in users)
    {
        if(users[x].isActive == true)
            {
                console.log(`${users[x].name} is active`)
              
            }
        else
            {
                console.log(`${users[x].name} is inactive`) 
            }   
    }
